		</ul>
		
		<p>
			A utilização do modo imperativo (“observarão”) na lei tem o propósito de esclarecer que as decisões dos tribunais, em qualquer questão jurídica (ou seja, relacionada à interpretação das leis, da Constituição e demais atos normativos do sistema jurídico), uma vez decidida por um tribunal, adotando um dos instrumentos que estão listados no texto, essa interpretação do direito passa a ser obrigatória para todos os juízes subordinados aos tribunais. 
		</p>
		<p >
			Assim, uma decisão do STF, desde que tomada por intermédio de um dos procedimentos contidos no art. 927 se torna obrigatória para qualquer juiz ou tribunal do país, eis que todos lhe são subordinados. Uma decisão tomada pelo STJ é obrigatória para os tribunais de justiça e os tribunais regionais federais. Uma decisão do Tribunal de justiça é obrigatória para os juízes de direito daquele estado e, do tribunal regional federal, para os juízes federais daquela região. 
		</p>
		<p >
			Além dos cinco incisos transcritos acima, há mais uma categoria de decisão em relação à qual os autores e o próprio Supremo Tribunal Federal concordam que têm caráter vinculante: as decisões do STF em julgamentos de recursos extraordinários com repercussão geral. Assim, temos um sexto item a ser acrescido aos que constam acima. 
		</p>
		<p >
			É preciso perceber que, no caso em que a decisão é tomada, qualquer decisão judicial é obrigatória. A discussão de precedentes vai além disso. Ela pretende tornar obrigatória a compreensão jurídica estabelecida em um caso anterior (daí a palavra precedente, ou seja, que vem antes) para outros casos, idênticos ou análogos a ele, que ainda não foram julgados. 
		</p>
			Nesse sentido, qualquer área (ramo) do Direito é apta a ter precedentes obrigatórios. O que determina a sua existência é o procedimento utilizado para a sua aprovação. Se algum desses 6 mecanismos for utilizado para produzir o precedente, a compreensão jurídica nele estabelecida será obrigatória. Caso contrário, ela terá força persuasiva, isto é, poderá ser usada como argumento para convencer o juiz de outro caso, mas ele ainda terá liberdade para decidir de modo diferente.  
		</p>     
		<p>	
			A inovação do CPC de 2015 reside no fato de que, até então, o caráter obrigatório dos precedentes estava ligado apenas aos tribunais de cúpula, ou seja, ao STF, ao STJ e aos demais tribunais superiores. Em 2015, essa possibilidade foi generalizada. Qualquer tribunal do país pode produzir precedentes obrigatório. Esse fato torna tão relevante a coleta, a análise e a compreensão dos precedentes, que se proliferarão daqui por diante e, seguramente, em poucos anos, mudarão a forma como o Direito brasileiro é aplicado e estudado nas faculdades. 
		</p>
