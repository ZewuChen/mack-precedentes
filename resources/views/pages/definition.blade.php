        <p align="justify" >Dispõe o art. 927 do Código de Processo Civil: </p>

		<p align="justify">Art. 927.  Os juízes e os tribunais observarão:</p>
		
		<ul>
			<li> I - as decisões do Supremo Tribunal Federal em controle concentrado de constitucionalidade;</li> 
			<li> II - os enunciados de súmula vinculante;</li> 
			<li> III - os acórdãos em incidente de assunção de competência ou de resolução de demandas repetitivas e em julgamento de recursos extraordinário e especial repetitivos;</li> 
			<li> IV - os enunciados das súmulas do Supremo Tribunal Federal em matéria constitucional e do Superior Tribunal de Justiça em matéria infraconstitucional;</li> 
